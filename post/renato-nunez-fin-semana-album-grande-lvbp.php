<h2>La “antología jonronera”: 3 y luego 1, sin escalas</h2>
<p>El viernes, contra <strong>Cardenales de Lara</strong>, Núñez se fue de 5-3 con <strong>tres cuadrangulares</strong> y tres impulsadas en la victoria 11-6. No es solo la línea: es la rareza del suceso. Un juego de tres jonrones suele ser, por sí mismo, una página completa. Pero Núñez no lo dejó como episodio aislado: el sábado, ante <strong>Tiburones de La Guaira</strong>, volvió a sacar la pelota —jonrón solitario— y cerró de 4-2 con dos anotadas en el triunfo 6-3.</p>
<p>Ese encadenado es lo que convierte el fin de semana en registro histórico: <strong>cuatro jonrones en dos juegos</strong>, con Magallanes usando a Núñez en turnos de producción (sexto y séptimo bate), como si el lineup estuviera diseñado para que el daño llegara en oleadas.</p>
<h2>Un club diminuto: Jim Pendleton y Eliézer Alfonzo como espejos</h2>
<p>Lo más llamativo es el espejo histórico: solo <strong>Jim Pendleton (Leones del Caracas)</strong> en 1960–61 y <strong>Eliézer "El Matatán" Alfonzo (Caribes de Anzoátegui)</strong> en 2004 habían logrado antes esa combinación de <strong>tres jonrones en un juego y uno más al día siguiente</strong>. No es una estadística de colección: es una señal de lo excepcional. En una liga de décadas, con parques distintos, pelotas distintas y contextos distintos, el patrón se repite apenas tres veces.</p>
<p>Y por eso el dato pesa: Núñez no solo tuvo poder; tuvo <strong>continuidad</strong> del poder, que es lo que separa una noche caliente de una racha con huella.</p>
<h2>Lo que significa para Magallanes en la pelea grande</h2>
<p>En diciembre, el jonrón no es solo una carrera: es un mensaje. El de Núñez llega cuando Magallanes necesita victorias que no se negocien, juegos donde el rival no tenga chance de respirar. Y también deja una lectura íntima para el fanático turco: se vio “como el año pasado”, como ese bateador capaz de cargar el ataque sin pedir permiso.</p>
<p>Si el cierre de ronda regular es un examen de nervios, Núñez respondió con el lenguaje más contundente del béisbol: <strong>la pelota en la calle</strong>. Porque hay fines de semana que pasan… y hay otros que se quedan a vivir en la historia.</p>
