<h3>2. Águilas barren a Bravos y se afianzan en la cima (ÁG 6–3 BRA)</h3>

<p>En Guatamare, la historia fue distinta: menos caos, más control. <strong>Águilas del Zulia</strong> aplicó una receta que se ha vuelto habitual en esta temporada: madrugar al abridor rival y luego administrar la ventaja con un pitcheo estable.</p>

<p>Ante el zurdo <strong>Félix Doubront</strong>, los rapaces armaron un <strong>rally de tres carreras en el primer inning</strong>: elevado de sacrificio de <strong>José Pirela</strong>, sencillo impulsor de <strong>Angelo Castellano</strong> y triple de <strong>Eduardo Torrealba</strong>. A partir de ahí, el partido se jugó en el ritmo que más le conviene a Zulia: arriba en la pizarra y con libertad para que su lineup ataque sin presión.</p>

<p>Torrealba terminaría con <strong>dos triples y múltiples remolques</strong>, mientras Castellano ligó <strong>de 4-3 con extrabases</strong>, confirmando la tendencia de la semana: no todo pasa por Pirela y los bates “históricos”; hay apoyo real desde los nombres que llegaron en cambios recientes.</p>

<p>En la lomita, <strong>José Dávila (2–1)</strong> firmó una de esas aperturas que no son espectaculares en el boxscore, pero sí profundamente útiles: <strong>5.2 innings de una sola carrera limpia</strong>, manteniendo siempre a Bravos cuesta arriba. El bullpen zuliano permitió algunas amenazas insulares, pero sin dejar que el juego se desbordara.</p>

<p>Con este <strong>6–3</strong>, Águilas:</p>

<ul>

<li>Llega a <strong>18 victorias</strong> y se consolida como <strong>líder solitario</strong> de la 2025-26.</li>

<li>Completa <strong>barrida de fin de semana</strong> ante un rival directo.</li>

</ul>

<p>Para Bravos, la lectura es más amarga: la serie en casa frente al puntero terminó siendo una llamada de atención. Su pitcheo abridor quedó expuesto y, aunque el lineup sigue produciendo a rachas, la defensa y la inconsistencia desde la loma están costando juegos clave en la zona media de la tabla.</p>



<hr>

<h3>3. Acuña sale del banco y Cardenales termina de hundir a Caribes (CARL 5–4 CARI)</h3>

<p>En Barquisimeto, <strong>Cardenales de Lara</strong> ratificó que su “semana de resurrección” va en serio. No solo le ganó la serie a <strong>Caribes de Anzoátegui</strong>: la barrió, y el último triunfo tuvo un guion perfecto para un equipo que necesitaba un golpe anímico.</p>

<p>Caribes dominaba el juego 3–1, apoyado en el poder de <strong>Balbino Fuenmayor</strong> y un elevado de sacrificio de <strong>Hernán Pérez</strong>. Pero la ventaja indígena empezó a diluirse en el sexto, cuando un doble de <strong>Yonny Hernández</strong> y un wild pitch abrieron la puerta al empate 3–3.</p>

<p>El momento clave llegó en el séptimo episodio: <strong>Luisangel Acuña</strong>, que venía de perderse juegos tras un pelotazo, salió como bateador emergente y cambió el partido de un solo swing. Su <strong>triple de dos carreras</strong> volteó el marcador 5–3 y encendió al Antonio Herrera Gutiérrez. Para un jugador que había estado en duda físicamente, fue casi una declaración: sigue siendo uno de los pilares del proyecto cardenal.</p>

<p>Desde el pitcheo, <strong>Max Castillo</strong> dio seis entradas de combate (con dos jonrones permitidos), y el relevo encabezado por <strong>Edgar Navarro (3–1)</strong> y rematado por <strong>Christian Cosby (1er salvado)</strong> sostuvo la diferencia pese al último intento de reacción oriental en el noveno (5–4 final).</p>

<p>El resultado deja a <strong>Cardenales en 16–16</strong>, empatando con Bravos en el <strong>cuarto lugar</strong>, y a <strong>Caribes</strong> tocado tras una barrida que lo baja un escalón en la pelea con Tigres y Águilas por la parte alta. Para Lara, más allá del récord, la señal es clara: el equipo ya no solo gana por batazos largos; también sabe remontar con dobles, toques, corrido agresivo y un bullpen que empieza a mostrarse confiable en juegos cerrados.</p>



<hr>

<h3>4. Tigres pasan la aplanadora sobre Magallanes (TIG 14–5 MAG)</h3>

<p>La jornada cerró en Valencia con una paliza que terminó de encender todas las alarmas en <strong>Navegantes del Magallanes</strong>. <strong>Tigres de Aragua</strong> se impuso <strong>14–5</strong> en el José Bernardo Pérez en un juego donde la combinación de ofensiva aragüeña y errores navieros dejó al descubierto la brecha actual entre ambos proyectos.</p>

<p>El lineup de Ozzie Guillén fue implacable:</p>

<ul>

<li><strong>Alberth Martínez</strong> disparó un <strong>jonrón de tres carreras</strong>, terminó con cuatro impulsadas y tres anotadas.</li>

<li><strong>Eduardo Escobar</strong> aportó otro <strong>bambinazo de tres rayitas</strong>, consolidando su rol de bate de poder experimentado.</li>

<li><strong>Leobaldo Cabrera</strong> añadió tres remolques para completar una noche de terror para el pitcheo turco.</li>

</ul>

<p>En total, Tigres ligó <strong>18 hits</strong>, fabricó carreras en varios episodios y aprovechó al máximo los <strong>cuatro errores defensivos de Magallanes</strong>, que se tradujeron en innings extendidos y más turnos de daño para la parte gruesa del lineup aragüeño.</p>

<p>En contraste, el trabajo de <strong>Jean Pinto (2–2)</strong> fue casi quirúrgico: <strong>5.2 innings sin carreras limpias</strong>, apenas tres hits permitidos, tres boletos y la sensación de que el juego estuvo bajo su control mientras estuvo en la lomita. Con una efectividad que ronda <strong>2.28</strong>, se consolida como uno de los brazos más fiables de la rotación.</p>

<p>El impacto en la tabla es directo:</p>

<ul>

<li><strong>Tigres</strong> se mantienen firmes en el <strong>segundo lugar</strong>, con algo como <strong>18–13</strong>, pegados a la espalda de Águilas.</li>

<li><strong>Magallanes</strong>, en cambio, cae a <strong>12–20</strong> y se convierte en el <strong>primer equipo con 20 derrotas</strong> en la campaña, comprometiendo seriamente sus opciones incluso de entrar por comodín.</li>

</ul>

<p>La palabra “crisis” ya no es exageración: el equipo combina fallas defensivas, un pitcheo abridor irregular y una ofensiva que reacciona tarde o cuando el daño es casi irreparable.</p>



<hr>

<h3>Así queda el tablero tras el domingo</h3>

<p>La noche del 23 de noviembre dejó una fotografía bien definida:</p>

<ul>

<li><strong>Águilas</strong> se afianzan como <strong>líderes</strong> con un discurso claro: rotación estable, bullpen funcional y un lineup que responde en distintas manos.</li>

<li><strong>Tigres</strong> se mantienen como el perseguidor más serio, con una ofensiva profunda y abridores en buen momento.</li>

<li>En la <strong>zona media</strong>, <strong>Cardenales</strong>, <strong>Bravos</strong>, <strong>Leones</strong>, <strong>Caribes</strong> y unos <strong>Tiburones</strong> al alza se reparten golpes en una pelea cerrada donde cada serie directa pesa como oro.</li>

<li><strong>Magallanes</strong>, de momento, mira todo eso desde el fondo, obligado a reaccionar pronto si quiere que la temporada 2025-26 no se le escape antes de tiempo.</li>

</ul>

<p>Más que una jornada más, el domingo pareció un adelanto de lo que será la recta final: remontadas, barridas y un margen de error cada vez más pequeño para todos.</p>
