<h2>Un Caracas paciente que sabe ganar en la carretera</h2>

<p>En Maracay, Leones del Caracas mostró otra cara de la misma moneda: no fue una paliza ni un rally escandaloso, sino un trabajo de hormiguita inning a inning para doblegar 6-4 a Tigres de Aragua en el José Pérez Colmenares. Es el tipo de victoria que construye confianza para jugar fuera de casa.</p>



<p>Caracas hizo daño de forma constante: anotó en la segunda, tercera, cuarta, sexta y novena entradas. Más que un inning grande, lo que hubo fue una ofensiva equilibrada que supo aprovechar oportunidades. Yonathan Daza volvió a mostrarse como pieza clave, con jonrón y remolques, mientras Jhonny Pereda y José Rondón aportaron ese bateo profesional que tanto se valora en un lineup que aspira a cosas grandes. El cuadrangular de Rondón en el noveno, para ampliar la ventaja, fue un golpe emocional justo cuando Tigres amenazaba con meterse de nuevo en el juego.</p>



<p>Del lado caraquista, el pitcheo también dejó nota positiva. Yoimer Camacho se apuntó la victoria y volvió a justificar su rol como brazo de confianza, y Norwith Gudiño firmó otro salvado para seguir consolidándose como cierre fiable. En una liga donde los bullpens suelen vivir al filo, tener un tándem que responda en la parte final es casi un lujo.</p>



<p>Tigres no fue un rival sencillo. Lorenzo Cedrola, Gorkys Hernández y Leobaldo Cabrera mantuvieron al equipo aragüeño en pelea con una ofensiva de 12 hits, incluyendo el jonrón de Cabrera y un doble productor de D’Orazio. El problema, una vez más, estuvo en la imposibilidad de completar la remontada. Es un patrón que se repite: lineup que pega, pero no termina de dar el golpe definitivo, y un pitcheo que permite carreras en demasiados capítulos.</p>



<p>Para Leones, la victoria mantiene un récord ganador y lo sostiene bien metido en la pelea de la parte alta de la tabla. Para Tigres, en cambio, deja la sensación de estar cerca… pero no lo suficiente.</p>



