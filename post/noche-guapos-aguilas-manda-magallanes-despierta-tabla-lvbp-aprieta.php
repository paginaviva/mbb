<h2>Caribes y un 8.º inning que puede cambiar la semana</h2>

<p>En Puerto La Cruz, Caribes de Anzoátegui firmó una de esas victorias que pueden servir como punto de quiebre anímico. Bravos de Margarita ganaba 3-0 y parecía tener controlado el compromiso, pero los orientales despertaron, empataron, se fueron arriba y remataron con un rally de cinco carreras en el octavo para sellar un 9-3 que pesa más por cómo se dio que por el margen en sí.</p>



<p>Bravos hizo el trabajo temprano: una carrera en el segundo inning y dos más en el cuarto para poner la pizarra 3-0. Hasta ahí, el libreto hablaba de un equipo que aprovechaba sus oportunidades y de un local que no encontraba la fórmula para descifrar al pitcheo margariteño. Pero Caribes, que ya sabe lo que es enfrentarse varias veces a este rival en pocas semanas, ajustó a tiempo.</p>



<p>La primera respuesta llegó en la baja del cuarto, cuando los indígenas empataron 3-3 y cambiaron por completo el ritmo del juego. A partir de ese momento, el relevo de casa se encargó de congelar a la ofensiva de Bravos, que volvió a conectar hits pero dejó de producir en el marcador. En el sexto, Caribes tomó ventaja 4-3. Y en el octavo, la ofensiva explotó: cinco carreras, producción repartida y, sobre todo, la noche grande de Omar Alfonzo con dos jonrones que lo colocan como una de las notas más interesantes del lineup anzoatiguense en esta etapa de la temporada.</p>



<p>Yorvin Pantoja se acreditó el triunfo desde el montículo, respaldado por un bullpen que supo cerrar la puerta sin sobresaltos en los últimos episodios. Del otro lado, la derrota para Carlos Suniaga refleja el golpe que significó ese octavo inning para un conjunto de Bravos que, cuando pierde, suele hacerlo con marcadores amplios y sensaciones de haber dejado ir el juego en un solo capítulo.</p>



<p>En la clasificación emocional, Caribes se mete de lleno en la pelea de mitad de tabla, recordándole al resto de la liga que en Puerto La Cruz no se regalan juegos, y que en cualquier momento su ofensiva puede armar un rally que cambie el panorama de una semana completa.</p>



