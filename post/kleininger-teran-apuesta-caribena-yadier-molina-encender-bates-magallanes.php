<h2>Un técnico hecho en el Caribe, con raíz criolla</h2>

<p>Aunque su formación inicial fue como jugador en el sistema de los Cardenales de San Luis, lo que define a Kleininger Terán hoy no es su etapa como antesalista o primera base, sino la manera en que abrazó el uniforme de coach desde muy joven. Cuando las lesiones y el techo competitivo le cerraron la puerta como pelotero, la misma organización que lo firmó lo recicló para los dugouts: primero como instructor en ligas menores, luego como parte fija de los procesos de desarrollo.</p>



<p>Ese salto temprano al mundo técnico le dio algo que no se ve todos los días en un hombre de 36 años: más de una década dedicada a enseñar, corregir y preparar juegos. Y aunque su rol en Estados Unidos lo ha mantenido cerca del pitcheo como bullpen catcher, su día a día lo ha acostumbrado a trabajar con información, a interpretar reportes y a ver el béisbol con lupa.</p>



<p>Pero donde realmente se construye su nombre en clave caribeña es en invierno. Puerto Rico fue la primera gran vitrina: inviernos en la Liga Roberto Clemente, paso por Atenienses de Manatí y, más recientemente, presencia en el cuerpo técnico de los Criollos de Caguas, con título y viaje a la Serie del Caribe incluidos. Esa experiencia, enfrentando a buena parte del talento que también circula por la LVBP, empezó a darle reputación de técnico serio, exigente y cercano al jugador.</p>



