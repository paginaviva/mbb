<p>Kleininger Terán sueña abiertamente con convertirse algún día en coach de Grandes Ligas, más allá del uniforme de bullpen catcher que viste en el norte. El paso por Puerto Rico, Dominicana y ahora Magallanes forma parte de esa ruta de crecimiento. Pero el presente no entiende de planes a futuro: la LVBP es una liga cortísima, y la nave necesita resultados ya.</p>



