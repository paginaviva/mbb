                <h2>El brazo que se puso al frente en la octava semana</h2>

<p>En una de las semanas más turbulentas para Leones del Caracas en esta zafra 2025-26, cuando el récord se les inclinaba peligrosamente hacia el lado negativo y el pitcheo estaba bajo juicio popular, apareció una figura que decidió no hundirse con el barco: <strong>Wilmer Font</strong>.</p>

<p>Entre el <strong>2 y el 7 de diciembre</strong>, el derecho de 35 años se subió dos veces a la lomita y en ambas salió con victoria, primero ante Tiburones de La Guaira y luego frente a Bravos de Margarita. En total, fueron <strong>11 innings de trabajo, apenas 2 carreras limpias, 6 hits permitidos, 10 ponches y 4 boletos</strong>, para una <strong>efectividad de 1.64 y WHIP de 0.91</strong> en el período. Números de as en cualquier liga, pero aún más ruidosos en una semana donde Leones apenas pudo sumar tres triunfos… y dos llevaron su firma.</p>

<p>Ese rendimiento no solo le dio aire al Caracas en la tabla; también le alcanzó para quedarse con el galardón oficial de la LVBP: <strong>Jugador de la Semana de la octava jornada</strong>, con el 38 % de los votos de la prensa acreditada.</p>

<h2>Un premio con sabor a reivindicación para los lanzadores</h2>

<p>En un circuito tradicionalmente inclinado a premiar la ofensiva —sobre todo en semanas de mucho bateo—, lo de Font tiene doble lectura. Por un lado, es la consecuencia lógica de un dominio claro: <strong>lideró la liga en victorias (2) y ponches (10)</strong> en ese lapso, y fue colíder en innings lanzados junto a Osmer Morales. Por otro, marca un punto de inflexión: es la <strong>primera vez desde la 2023-24 que un pitcher se lleva el premio semanal</strong>, y el <strong>primer lanzador venezolano de Leones</strong> en conseguirlo desde J.C. Ramírez (2022-23) y Jhoulys Chacín (2015-16).</p>

<p>En una franquicia donde el foco reciente ha estado en los bates de Aldrem Corredor, Harold Castro o Yonathan Daza —todos ganadores del mismo premio esta campaña—, ver a un abridor capitalino subir al podio manda otro mensaje: la rotación también puede ser protagonista, incluso en medio de una campaña complicada.</p>

<h2>La pelea en la boleta: Font, Pirela y Mata</h2>

<p>La votación no fue un paseo. Detrás de Font apareció un bateador de peso: <strong>José Pirela</strong>, motor de Águilas del Zulia, que se quedó con el 25 % de los votos tras una semana de <strong>8 empujadas, 6 anotadas y línea .346/.393/.577</strong>, liderando varios departamentos ofensivos.</p>

<p>El tercer escalón lo ocupó <strong>Bryan Mata</strong> (15 %), as de Navegantes del Magallanes, quien firmó una apertura de lujo: <strong>6.0 innings en blanco, solo 1 hit permitido, 7 ponches y 2 boletos</strong> ante Tigres. Un outing que, en cualquier otra semana menos cargada de buenas actuaciones, habría sido candidato natural al premio.</p>

<p>Que un abridor se impusiera sobre un bateador tan completo como Pirela y otro pitcher dominante como Mata dice mucho del contexto: <strong>Caracas necesitaba detener la hemorragia</strong>, y Font lo hizo dos veces en menos de una semana.</p>

