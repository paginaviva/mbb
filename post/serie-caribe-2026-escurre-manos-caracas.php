<p>En lo económico, el golpe es doble: se pierde el flujo de turismo deportivo y la exposición internacional que implica una semana de foco mediático en la ciudad. En lo deportivo, queda la sensación de que la LVBP, pese a la calidad de su campeonato, paga cuentas que no generó en el terreno.</p>

