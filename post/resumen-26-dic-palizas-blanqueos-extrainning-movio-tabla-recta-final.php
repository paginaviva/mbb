<h2>Una jornada que se jugó con calculadora… y con carácter</h2>



<p>A estas alturas del calendario, la LVBP deja de ser “temporada” y se convierte en <strong>examen de nervios</strong>. Los equipos ya no se miden por rachas lindas o por el “buen béisbol” de noviembre, sino por su capacidad de fabricar carreras cuando el rival no regala nada y, sobre todo, por la consistencia para evitar el inning grande en contra.</p>



<p>Por eso la jornada fue tan reveladora: mostró dos caminos opuestos hacia la postemporada. Uno, el de la contundencia ofensiva, como la de los <strong>Navegantes del Magallanes</strong> y los <strong>Cardenales de Lara</strong>, capaces de convertir un pequeño resbalón del contrario en un rally que rompe el juego. El otro, el de la precisión, como la de <strong>Caribes de Anzoátegui</strong>, que entendió que un 2-0 puede valer lo mismo que un 12-3 si el bullpen cierra la puerta.</p>



<p>Y en el medio quedó el béisbol más cruel: el extrainning que no se decide por un jonrón sino por <strong>boletos</strong>, pelotazos y un lanzador buscando la zona como quien busca aire.</p>



<h2>Magallanes vs Tiburones: cuando el inning grande es una sentencia</h2>



<p>El <strong>17-6</strong> de los <strong>Navegantes del Magallanes</strong> sobre los <strong>Tiburones de La Guaira</strong> no fue solo una paliza; fue una declaración. En diciembre, los equipos no ganan “bonito”: ganan <strong>con intención</strong>, y Magallanes encontró la suya en un segundo inning que se convirtió en el tipo de capítulo que define temporadas.</p>



<p>El rally de <strong>ocho carreras en el 2do inning</strong> fue el resumen perfecto de lo que suele separar a un clasificado de un equipo que se queda mirando: <strong>poder + oportunismo</strong>. Allí aparecieron los batazos largos de <strong>Luis Sardiñas (Navegantes del Magallanes)</strong> y <strong>Renato Núñez (Navegantes del Magallanes)</strong>, y el doble barre-bases de <strong>Rougned Odor (Navegantes del Magallanes)</strong>, el tipo de swing que no solo impulsa carreras: también <strong>cambia la respiración de un estadio</strong>.</p>



<p>Pero hay un ángulo igual de importante: Magallanes no necesitó que todo fuera perfecto para despegar. Aprovechó el desorden rival —errores defensivos y bases por bolas— como lo hacen los equipos que huelen octubre/enero desde temprano: cuando el contrario se daña solo, tú no lo rescatas; tú lo castigas.</p>



<p>En la lomita, <strong>Ricardo Sánchez (Navegantes del Magallanes)</strong> puso orden en el guion. Su línea de 5.1 innings, 7 ponches y el control emocional para navegar pasajes con tráfico (incluyendo boletos) le dio a los turcos lo que más vale en un juego de paliza: <strong>tranquilidad para administrar brazos</strong> y mantener la ventaja sin drama. No siempre el abridor de una vapuleada se lleva el titular; esta vez sí, porque en jornadas de alto voltaje, el que estabiliza temprano evita que el partido se convierta en una ruleta.</p>



<p>Para <strong>Tiburones de La Guaira</strong>, el marcador es un espejo incómodo. En el cierre de la eliminatoria, regalar outs o regalar bases es como regalar innings. Y si a eso se le suma que el rival te mete un inning grande, lo que sigue es cuesta arriba con la pendiente más cruel: la emocional. El reto aquí no es “olvidar la paliza”, sino corregir lo que la provoca: <strong>ejecución defensiva, control del pitcheo y respuestas cuando el juego se te va en una ráfaga</strong>.</p>



<h2>Caribes vs Águilas: el béisbol de dos carreras que vale oro</h2>



<p>Mientras en Caracas el marcador parecía pizarra de softbol, en otro frente la jornada se escribió con la tinta más fina: <strong>Caribes de Anzoátegui</strong> venció <strong>2-0</strong> a las <strong>Águilas del Zulia</strong>, y lo hizo con una receta que en postemporada suele ser más confiable que cualquier alineación explosiva: <strong>anotar temprano, ampliar cuando se pueda y cerrar sin concesiones</strong>.</p>



<p>La primera carrera llegó en el 1er inning con una secuencia que habla de agresividad bien entendida. <strong>Herlis Rodríguez (Caribes de Anzoátegui)</strong> anotó tras una jugada que incluyó robo de base y el hit impulsor de <strong>Hernán Pérez (Caribes de Anzoátegui)</strong>. Esas son las carreras que no siempre aparecen en highlights, pero que en diciembre pesan como un jonrón: son carreras de <strong>lectura</strong>, de presión constante, de obligar a la defensa a ejecutar.</p>



<p>La segunda cayó en el 6to inning: doble de <strong>Jesús Sucre (Caribes de Anzoátegui)</strong> y elevado de sacrificio de <strong>Romer Cuadrado (Caribes de Anzoátegui)</strong>. En lenguaje de playoff, eso se traduce así: “te saco una rayita extra sin necesidad de un hit con gente en base”. Y cuando tu plan es ganar 2-0, esa rayita es una póliza de seguro.</p>



<p>El eje del juego, sin embargo, fue el pitcheo. <strong>Harol González (Caribes de Anzoátegui)</strong> trabajó 6.0 innings como columna vertebral del blanqueo, y el bullpen —con <strong>Riskiel Tineo (Caribes de Anzoátegui)</strong> incluyendo un tramo de cuatro outs— terminó de sellar el partido. Caribes conectó 10 hits y limitó a Águilas a 5: no fue un juego sin tráfico, fue un juego sin grietas.</p>



<p>Del lado zuliano, cargarle la derrota a <strong>Wendolyn Bautista (Águilas del Zulia)</strong> es lo que dice la tarjeta, pero el mensaje es colectivo: cuando el rival te gana con apenas dos carreras, lo que te está diciendo es que <strong>no pudiste crear caos</strong>, no pudiste correr, no pudiste encadenar turnos, no pudiste romper la narrativa del pitcher. Y en una liga donde los juegos apretados definen el futuro inmediato, ese tipo de silencio ofensivo se siente como un presagio.</p>



<h2>Tigres vs Bravos: el extrainning que se decide con paciencia… y con pulso</h2>



<p>Si alguien quería un retrato de la LVBP en su fase más nerviosa, allí estaba el <strong>2-1 en 10 innings</strong> de los <strong>Tigres de Aragua</strong> sobre los <strong>Bravos de Margarita</strong>. Nueve innings en cero, tensión acumulada, el décimo como juicio final y una conclusión tan beisbolera como cruel: no ganó el que más bateó, sino el que <strong>controló mejor el caos</strong>.</p>



<p>En la alta del 10mo, Bravos abrió la puerta con una carrera que llegó por lanzamiento desviado de <strong>Jonathan Vargas (Tigres de Aragua)</strong>, anotando <strong>Moisés Gómez (Bravos de Margarita)</strong>. En extrainnings, esas carreras “raras” son veneno: no solo te ponen abajo, sino que te obligan a fabricar sin margen de error.</p>



<p>Y allí Aragua respondió con un inning que fue más de cabeza que de fuerza. Con <strong>Eduardo Escobar (Tigres de Aragua)</strong> como corredor en segunda (la dinámica del extrainning cambia el béisbol completo), apareció el hit de <strong>José Peraza (Tigres de Aragua)</strong>, luego el pelotazo a <strong>Alberth Martínez (Tigres de Aragua)</strong>, y el empate lo trajo <strong>Jermaine Palacios (Tigres de Aragua)</strong> con el batazo oportuno. Esa secuencia no se trata de “pegar hits”: se trata de <strong>no desesperarse</strong>.</p>



<p>La victoria se definió con boletos consecutivos, incluyendo el boleto con bases llenas de <strong>Leobaldo Cabrera (Tigres de Aragua)</strong>. Suena antiépico, pero en realidad es todo lo contrario: en el béisbol de verdad, el que gana también sabe <strong>dejar pasar</strong>. Un boleto con las bases llenas es un checkmate donde el bateador no necesita swing: necesita <strong>disciplina y temple</strong>.</p>



<p>El dato de contexto que no se puede ignorar: esta clase de juego, además de mantener viva la opción aragüeña, se conectó con el desenlace que dejó sin ruta a <strong>Leones del Caracas</strong>. Y allí la LVBP muestra su rostro más duro: a veces tu temporada no se muere con tu último out, sino con el boleto que otorga otro equipo en otro estadio.</p>



<p>Como nota que humaniza la noche, <strong>Lorenzo Cedrola (Tigres de Aragua)</strong> llegó a 100 partidos con el club en el período citado. En una jornada de presión, esos hitos recuerdan que las temporadas se construyen con constancia… y se deciden con detalles.</p>



<h2>Cardenales vs Leones: un slugfest que coronó al líder y cerró una puerta histórica</h2>



<p>El <strong>15-13</strong> de <strong>Cardenales de Lara</strong> sobre <strong>Leones del Caracas</strong> fue un juego de esos que no se “analizan” sin respirar: fue un festival ofensivo, un intercambio de golpes y, al final, una confirmación de jerarquía. Lara aseguró el <strong>primer lugar</strong> de la tabla y, en la misma onda expansiva, Caracas quedó matemáticamente fuera.</p>



<p>Cardenales conectó <strong>19 hits</strong> y produjo con extrabases, incluyendo jonrones. Pero el nombre propio de la noche, el que se ganó el encabezado sin discusión, fue <strong>Danry Vásquez (Cardenales de Lara)</strong>: jornada perfecta <strong>4-4</strong>, <strong>2 jonrones</strong>, <strong>6 carreras impulsadas</strong> y un boleto para completar la idea de “no me sacaron de circulación”. En una liga donde cualquiera puede tener una gran noche, lo de Vásquez fue más que eso: fue una actuación que resume lo que un equipo líder suele tener en diciembre: <strong>un bate capaz de cargar un juego grande cuando la presión sube</strong>.</p>



<p>A su lado aparecieron también los batazos de vuelta completa de <strong>Yohendrick Piñango (Cardenales de Lara)</strong> y <strong>Jesús Bastidas (Cardenales de Lara)</strong>. Esa es otra señal de un líder: no depende de un solo nombre, sino de una alineación que, en cualquier tramo del orden, puede cambiar la pizarra con un swing.</p>



<p>Para <strong>Leones del Caracas</strong>, perder 15-13 es doblemente doloroso porque te deja sin el consuelo del “no bateamos”. Caracas hizo daño, pero el partido le exigía otra cosa: detener el sangrado, evitar el inning extendido, apagar el rally antes de que se convierta en incendio. En juegos de alta anotación, el pitcheo no necesita dominar: necesita <strong>contener</strong>, y cuando no lo logra, el resultado suele ser una eliminación que pesa en el pecho de una fanaticada que no está acostumbrada a despedidas tempranas.</p>



