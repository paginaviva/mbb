<p>Porque en la LVBP, el final de la eliminatoria no es un cierre: es una puerta giratoria. <strong>Los que entran lo hacen con impulso; los que se quedan afuera lo hacen con preguntas.</strong> Y en este béisbol nuestro, los grandes capitanes no se retiran: simplemente cambian de trinchera.</p>
