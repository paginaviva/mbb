<h2>De descarte a figura: el camino de Antonio Piñero en la LVBP</h2>
<p><strong>Antonio Piñero</strong>, nacido en Bejuma en 1999, fue originalmente firma de <strong>Leones del Caracas</strong> en la LVBP. Con los melenudos apenas consumió <strong>22 turnos en la 2021-2022</strong>, con 2 hits y promedio de .083, antes de ser enviado a <strong>Navegantes del Magallanes</strong> en un cambio donde también apareció <strong>Romer Cuadrado</strong>. En el Buque nunca llegó a debutar y terminó fuera de los planes.</p>
