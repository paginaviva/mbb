<h2>El factor clave: actitud, plan de ataque y simplificación del swing</h2>
<p>En la entrevista reciente, <strong>Frank Díaz</strong> pone el foco en algo que no aparece en el boxscore: la <strong>disposición de Antonio Piñero para escuchar, aprender y trabajar</strong>. Lo describe como un pelotero tranquilo, receptivo a los consejos y con buena energía diaria en el clubhouse. Para un jugador que siempre fue valorado primero por el guante, ese cambio de chip es fundamental: aceptar que había que rediseñar el bateo para poder quedarse.</p>
<p>Desde lo técnico, el trabajo con René Reyes y Díaz pasó por <strong>simplificar la mecánica</strong>: menos movimiento de manos en la carga, base más firme en la parte baja del cuerpo y un swing más directo hacia la pelota. Al reducir “ruido” en el swing, Piñero gana tiempo, algo clave para un bateador que ahora ve más rompientes y cambios en conteos de bateador.</p>
<p>El otro punto es el <strong>plan de ataque</strong>. Piñero dejó de intentar hacer demasiado con cada swing. Hoy se le ve concentrado en buscar pitcheos manejables temprano en el conteo y en usar todo el campo, especialmente la banda contraria cuando los lanzadores lo atacan afuera. La consecuencia es un porcentaje alto de contactos sólidos en líneas y rodados fuertes, en lugar de elevados fáciles.</p>

