                <p>La noche del viernes 21 de noviembre dejó una de esas jornadas que mueven más que la tabla: ajustó narrativas. Tigres volvió a la cima, Leones salió del fondo, Cardenales se sacudió a punta de batazos y Bravos defendió la casa para frenar a un Magallanes que otra vez se quedó corto con corredores en base. Cuatro juegos, cuatro historias distintas… todas con impacto directo en la lucha por los cupos a enero.</p>

