<h2>La noche en que Rougned Odor reescribió el libreto en Puerto La Cruz</h2>

<p>En Puerto La Cruz, lo que se vio fue otra cosa: un juego que empezó como <strong>paliza de un lado</strong>, se convirtió en <strong>remontada del otro</strong>, y terminó siendo un espectáculo ofensivo de <strong>20–11</strong> a favor de <strong>Navegantes del Magallanes</strong> sobre <strong>Caribes de Anzoátegui</strong>, con <strong>34 hits combinados</strong> y una actuación individual que se va directo al archivo histórico.</p>

<p>Magallanes salió encendido. <strong>Carlos Rodríguez (Navegantes del Magallanes)</strong> abrió la noche con <strong>jonrón en el primer inning</strong> y el lineup turco se encargó de construir una ventaja de <strong>7-0</strong> que parecía sentenciar temprano. Pero esto es LVBP: la ventaja grande antes de la mitad del juego suele ser apenas el prólogo.</p>

<p>Entre el cuarto y el sexto inning, <strong>Caribes de Anzoátegui</strong> respondió con toda la furia de su ofensiva. Con <strong>Jesús Sucre</strong> y <strong>Carlos Pérez (Caribes de Anzoátegui)</strong> como protagonistas, la Tribu fabricó <strong>11 carreras</strong> y volteó la pizarra a un sorprendente <strong>11-7</strong>, exponiendo las dudas de un bullpen turco que no terminaba de cerrar las heridas.</p>

<p>Entonces el juego cambió de manos definitivamente… y con sello de un solo apellido: <strong>Rougned Odor (Navegantes del Magallanes)</strong>.</p>

<p>En el <strong>octavo inning</strong>, con el juego 11-9 a favor de Caribes, Magallanes llenó las bases y Odor respondió con un <strong>doble clave</strong> que no solo igualó, sino que puso arriba a los Navegantes. El inning se convirtió en un <strong>rally de siete carreras</strong>, gracias también a los dobles de <strong>Luis Sardinas</strong> y <strong>Luis Suisbel (Navegantes del Magallanes)</strong>, más otro batazo oportuno de <strong>Carlos Rodríguez</strong>, para voltear el score a <strong>16-11</strong>.</p>

<p>En el <strong>noveno</strong>, Odor volvió a escena con un <strong>jonrón de dos carreras</strong>, y justo después <strong>Leandro Pineda (Navegantes del Magallanes)</strong> la sacó también, rematando el marcador <strong>20-11</strong> y dejando sin oxígeno a Caribes.</p>

<p>La línea final de <strong>Rougned Odor</strong> es de videojuego: <strong>5-3</strong>, con <strong>dos dobles, un jonrón, tres anotadas, siete impulsadas y dos bases robadas</strong>. Igualó el <strong>récord de franquicia de 7 carreras impulsadas en un juego</strong> y, de paso, llegó a <strong>12 jonrones y 10 bases robadas en la temporada</strong>, convirtiéndose en <strong>uno de los pocos peloteros en la historia de la LVBP con campaña 10-10</strong> en esos renglones. Noche de estrella y de estadístico.</p>

<p>Magallanes también contó con una exhibición de <strong>Carlos Rodríguez</strong>, que terminó <strong>7-5</strong>, con jonrón, tres anotadas y dos empujadas, consolidando un promedio que ya ronda la élite del torneo. <strong>Leandro Pineda</strong> sumó <strong>dos jonrones y tres impulsadas</strong>, mientras que <strong>Luis Sardinas</strong> y <strong>Luis Suisbel</strong> hicieron daño con sus dobles en el rally decisivo.</p>

<p>Del lado de Caribes, más allá de la derrota, quedó el esfuerzo de <strong>Jesús Sucre</strong>, quien se fue de 4-3 con jonrón y cinco remolcadas antes de salir del juego por pelotazo en la mano. El problema para la Tribu estuvo en el pitcheo: el bullpen, con nombres como <strong>Harold Chirino</strong>, no encontró la manera de cerrar la puerta en el último tercio y terminó permitiendo <strong>13 carreras entre el séptimo y el noveno inning</strong>, un golpe durísimo en cualquier contexto, pero letal en plena pelea por la clasificación.</p>



