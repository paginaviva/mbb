<h2>Magallanes blanquea y barre: el pitcheo se pone la gorra de líder</h2>

<p>En Valencia, <strong>Magallanes 4 – 0 Águilas</strong> no fue solo una victoria más: fue la confirmación de que la racha turca dejó de ser accidente y empieza a parecer plan.</p>

<p>El juego se explica desde la lomita: <strong>Bryan Mata</strong> tiró <strong>5.0 entradas en blanco</strong>, con 5 hits permitidos, solo un boleto y 5 ponches. No fue un dominio aplastante, pero sí el tipo de apertura que cualquier mánager firma en esta etapa: atacar la zona, evitar el extrabase y obligar a Águilas a ligar varios batazos seguidos para hacer daño… cosa que nunca ocurrió.</p>

<p>Detrás de él, el relevo —<strong>Chirinos, Wílking Rodríguez, Franco y Vázquez</strong>— completó otras 4.0 entradas sin permitir anotaciones. Un <strong>blanqueo de manual</strong>, exactamente lo que necesitas para terminar de rematar una barrida ante el equipo que venía mandando en la tabla.</p>

<p>Del lado ofensivo, el nombre propio fue otra vez <strong>Rougned Odor</strong>: doble productor temprano, jonrón solitario en el sexto (su <strong>sexto de la zafra</strong>) y participación directa en <strong>3 de las 4 carreras</strong> del juego. Venía de sacarla el día anterior; ahora repite y confirma que su despertar ofensivo no es una chispa aislada, sino la carta que Magallanes esperaba para que ese lineup “bueno en el papel” empezara a verse bueno también en el boxscore.</p>

<p>La foto que deja la serie: <strong>Águilas, barridas en Valencia</strong> y con la cima un poco más apretada; <strong>Magallanes, en plena escalada</strong>, con un pitcheo que por fin acompaña el bombardeo jonronero de los últimos días.</p>

