<p>Es apenas el segundo triunfo de Bravos en la historia del Juego de La Chinita, un dato que le da aún más peso a la remontada. En la tabla, el impacto es inmediato: Bravos se monta en récord de 15-13, mientras Zulia queda en 15-11. Siguen como líderes, sí, pero con Caribes, Tigres y los propios insulares respirándoles en la nuca, a juego y medio o menos. No es lo mismo mirar el standing con dos o tres juegos de colchón que ver la cima comprimida por una jornada donde tu bullpen permite 13 carreras en tres innings.</p>



