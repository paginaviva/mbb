  <li>¿Aumenta la capacidad de Cardenales para producir carreras en juegos cerrados y con hombres en circulación?</li>

  <li>¿Logra el staff de coaches, con estas piezas, estabilizar un roster que hasta ahora ha tenido demasiados dientes de sierra?</li>

</ul>



<p>Lo que sí es seguro es que la afición larense tiene motivos renovados para ilusionarse. La barrida sobre Leones limpió el ambiente; la llegada de estos tres nombres le da argumento al optimismo. En una LVBP donde la tabla se aprieta jornada a jornada, quienes se mueven a tiempo marcan la diferencia entre ver el round robin desde el dugout o desde el sofá.</p>



<p>Cardenales ya hizo su jugada. Ahora le toca a Chiquillo, Bastidas y Piñango demostrar que no llegaron solo para llenar el lineup, sino para cambiarle el pulso a una temporada que, de a poco, empieza a parecerse a esas campañas donde Lara no se conforma con estar en la fiesta: quiere, otra vez, ser el último en apagar la luz.</p>
