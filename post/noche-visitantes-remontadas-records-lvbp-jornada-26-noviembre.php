<h2>Tiburones le devuelven el golpe al líder en Maracaibo</h2>

<p>Veinticuatro horas después del walk-off zuliano, La Guaira regresó al Luis Aparicio con la urgencia de quien sabe que no puede dejar que el líder se escape. Y respondió. Tiburones venció 7-4 a Águilas en un juego donde el ataque litoralense marcó la pauta desde temprano y el séptimo inning terminó de acomodar la batalla por la punta.</p>

<p>El equipo visitante pegó primero en el segundo capítulo, con un elevado de sacrificio de Carlos Tocci y un jonrón de dos carreras de Franklin Barreto que abrieron el marcador 3-0. En el tercero ampliaron a 4-0 con doble de Yangervis Solarte y hit impulsor de Pedro Castellanos, dibujando el tipo de juego que La Guaira no siempre ha sabido sostener: ventaja amplia, pitcher cómodo y margen para manejar el bullpen.</p>

<p>Pero Águilas es líder por algo, y lo demostró con poder. Un jonrón solitario de Andrés Chaparro y otro de dos carreras de Jaison Chourio, más una anotación adicional en el sexto, devolvieron el juego a la raya con empate 4-4. El Luis Aparicio volvió a ser caldera y por un rato pareció repetirse el libreto de la noche anterior.</p>

<p>La diferencia llegó en el séptimo, con Tocci como detonante. El jardinero abrió con triple, Tiburones encontró la manera de capitalizar errores y descontrol –incluyendo un wild pitch– y un sencillo de Daniel Montaño puso cifras definitivas. En la línea final, 3-3 para Tocci, jonrón de Barreto y una victoria que vale doble: frena al líder y le recuerda a todo el circuito que La Guaira tiene con qué pelear arriba si su ofensiva se mantiene así de profunda.</p>



