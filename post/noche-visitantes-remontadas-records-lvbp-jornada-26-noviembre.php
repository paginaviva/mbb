<h2>Amarista entra en la historia y Bravos firma la remontada del año</h2>

<p>En Puerto La Cruz, el guion fue de película. Caribes ganaba 6-2, parecía tener el juego controlado tras un jonrón de tres carreras de Andruw Monasterio, y la noche pintaba para fiesta oriental. Pero Bravos de Margarita se guardó lo mejor para el final: un noveno inning de siete carreras, coronado por grand slam de Ramón Flores, para voltear el marcador 9-6 y llevarse un triunfo que pesa en la tabla y en la memoria.</p>

<p>Antes de ese terremoto ofensivo, la noche ya era especial por otra razón. Alexi Amarista, emblema de Caribes y de la liga, llegó al juego a dos hits de los 800 en su carrera en la LVBP. Terminó de 5-4, superó la barrera de los 800 y siguió sumando hasta llegar a 802 imparables, entrando en ese club selecto de apenas un puñado de peloteros en la historia del circuito. Lograrlo, además, en el estadio que lleva el nombre de Alfonso “Chico” Carrasquel, no deja de tener un simbolismo especial.</p>

<p>El beisbol, sin embargo, no entiende de sentimentalismos cuando se trata del marcador. En el noveno, Bravos encadenó boletos, contactos oportunos y golpes grandes hasta llenar las bases con dos outs y dejar la escena servida para Flores. El capitán insular no perdonó: jonrón con la casa llena, giro completo al juego y un dugout visitante convertido en una avalancha de celebraciones.</p>

<p>La remontada no solo equilibra la serie después de la derrota del día anterior, también coloca a Margarita con récord parejo y de lleno en la pelea por puestos de clasificación. Y deja al “Pulpo” Amarista con un sabor agridulce: noche histórica en lo personal, pero manchada por una derrota que duele en la lucha de mitad de tabla.</p>



