<h2>Tigres: revancha y carácter en el Monumental</h2>

<p>Si algo dejó claro la serie Tigres–Leones en La Rinconada es que no hay margen para pestañear. El martes, Caracas se había quedado con el duelo. Este miércoles, Aragua se cobró la revancha con un 5-4 que se decidió en el noveno inning y que, de paso, mantuvo a los felinos a medio juego de la punta.</p>

<p>Leones parecía tener el control del compromiso. Tomó ventaja 2-0 temprano con sencillo de Liván Soto con las bases llenas, y aunque Tigres empató en el tercer episodio con doble de Gorkys Hernández y elevado de sacrificio de Jesús Rodríguez, los melenudos volvieron a irse arriba y llegaron al octavo con opción de cerrar la noche. El jonrón de Harold Castro en ese inning pareció ser el golpe de autoridad definitivo.</p>

<p>Pero el cierre contó otra historia. En el noveno, Tigres llenó las bases y Rodríguez volvió a ser protagonista: su rodado produjo el empate y un error en el tiro de Soto abrió la puerta para la carrera de la diferencia. No fue una explosión ofensiva, sino un inning trabajado, de presión y contacto, típico de un equipo que entiende cómo jugar con el marcador apretado.</p>

<p>Más allá del golpe anímico para Leones, el resultado refuerza la imagen de Aragua como club que no se cae ante la adversidad. Mantiene la presión sobre el liderato, demuestra que puede ganar en el Monumental y deja sembrada la duda sobre el manejo del bullpen capitalino en finales cerrados. Para un Caracas que pelea desde la parte baja de la tabla, estos juegos se sienten casi como eliminatorios adelantados.</p>



