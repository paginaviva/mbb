<h2>La regla “1 vs 2”: por qué el 5° juega con red y el 6° sin margen</h2>

<p>En el fondo, el Comodín es una especie de “play-in” con ventaja incorporada. <strong>Caribes</strong> tiene dos caminos para avanzar: ganar el 29 y listo. <strong>Tigres</strong> solo tiene uno: ganar el 29 para seguir vivo… y volver a ganar el 30 para clasificar. Eso afecta todo: desde la selección del abridor hasta el uso del bullpen.</p>

<p>Para el 5°, el plan puede ser agresivo pero con control: si el abridor no está fino, hay margen para gatillar temprano y exprimir los relevistas con la tranquilidad de que una victoria te basta. Para el 6°, el manejo se vuelve una apuesta: hay que ganar dos días seguidos, así que el bullpen no puede quedar destruido en el primer juego si quieres tener con qué cerrar el segundo.</p>

