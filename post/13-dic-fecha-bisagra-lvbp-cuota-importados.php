<h2>¿Qué cambia exactamente?</h2>
<p>La regla establece que <strong>después del 13/12/2025 ningún equipo podrá reducir el número de jugadores extranjeros con que habitualmente había venido jugando</strong>. El detalle importante es la palabra <strong>“habitualmente”</strong>, porque ahí está la trampa y también el control: la liga define ese “número habitual” con un criterio medible, no con percepciones.</p>
<p>En términos prácticos, se considera “habitual” la cantidad de importados que haya figurado en el roster semanal en <strong>al menos 75% de los juegos disputados hasta el 17 de diciembre (inclusive)</strong>. Es decir, tu historial reciente te persigue: lo que has venido usando será tu referencia, y desde este corte ya no puedes recortarlo a conveniencia.</p>

<h2>El mensaje de fondo: menos margen, más orden… y multas si te pasas de vivo</h2>
<p>La norma también deja claro que hay <strong>excepciones</strong>: casos fortuitos, fuerza mayor o situaciones extraordinarias. Pero la llave la tiene la liga: <strong>la Junta Directiva decide</strong> si la reducción está justificada. Y si considera que fue “manifiestamente injustificada”, el castigo no es simbólico: <strong>multa por cada importado retirado y por día</strong>, hasta que se corrija la infracción o hasta que termine la ronda eliminatoria.</p>
<p>Eso, en el día a día, obliga a las gerencias a jugar con cuidado: no solo se trata de “quién me ayuda hoy”, sino de <strong>qué cupo estoy fijando como mi normalidad</strong> para el cierre del mes.</p>

<h2>Por qué hoy se siente como un “día de transacciones”</h2>
