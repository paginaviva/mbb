<h2>Águilas camina en caliente y Tigres sigue en caída libre</h2>
<p>En Maracaibo, el libreto fue completamente distinto, pero igual de dramático. <strong>Águilas del Zulia</strong> necesitaba seguir sumando para mantenerse en la pelea, y lo consiguió a su manera favorita: dejando en el terreno a <strong>Tigres de Aragua 7-6</strong> con un racimo de tres carreras en el noveno inning.</p>
<p>Tigres había tomado la delantera temprano, 3-0, gracias a un doble barre-bases de <strong>Lorenzo Cedrola</strong>, que impulsó a <strong>Eduardo Escobar</strong>, <strong>José Peraza</strong> y <strong>José Sibrián</strong>. Pero Zulia respondió en el tercer episodio con un ataque que incluyó batazos de <strong>Jackson Chourio</strong>, <strong>Alí Castillo</strong>, <strong>Jaison Chourio</strong> y <strong>Andrés Chaparro</strong>, suficiente para empatar 3-3 y mandar el mensaje de que no sería una noche cómoda para el pitcheo aragüeño.</p>
<p>Un sencillo de <strong>Isaías Tejeda</strong> puso a Águilas arriba 4-3 en el quinto, pero Tigres reaccionó y volteó 5-4 con contacto de <strong>Gorkys Hernández</strong> y un error de tiro de <strong>Kenedy Corona</strong>, síntoma de que el equipo zuliano también tiene grietas. En el noveno, <strong>Silvino Bracho</strong> permitió otra rayita que parecía sentenciar el duelo 6-4 a favor de Aragua.</p>
<p>Sin embargo, el cierre fue una pesadilla para <strong>Ronnie Williams</strong>. El cerrador, que llegaba con una hoja de vida sólida en la campaña, explotó en el peor momento: permitió hits, concedió <strong>cuatro boletos, incluidas dos bases por bolas con las almohadillas llenas</strong>, y mezcló un wild pitch que abrió las puertas del empate. En ese caos controlado, <strong>Andrés Chaparro</strong> recibió un pasaporte que acercó a Águilas y, finalmente, <strong>Angelo Castellano</strong> negoció la base por bolas que trajo la carrera de la victoria y desató la fiesta en el Luis Aparicio.</p>
<p><strong>Chaparro</strong> terminó de 4-2 con dos impulsadas y se confirmó como una de las voces de carácter del clubhouse. <strong>Alí Castillo</strong>, por su parte, siguió sumando juegos de múltiples hits y llegó a <strong>más de 60 imparables en la ronda regular por cuarta vez en su carrera</strong>, una muestra del tipo de consistencia que lo ha convertido en figura histórica del Zulia. Los hermanos <strong>Jackson Chourio</strong> y <strong>Jaison Chourio</strong> aportaron chispa desde la juventud, en una ofensiva que combina experiencia y sangre nueva.</p>
<p>Para Águilas, fue la <strong>tercera victoria consecutiva</strong>, un envión anímico enorme en plena recta final. Para Tigres, en cambio, significó la quinta derrota al hilo y la confirmación de un desplome preocupante: después de iniciar 10-3, ahora exhibe un tramo reciente de 15-25 que pone en riesgo serio sus opciones de postemporada.</p>
