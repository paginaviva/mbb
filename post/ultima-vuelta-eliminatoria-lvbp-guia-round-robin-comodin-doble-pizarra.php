  <li><strong>Ten una ventana fija en Bravos–Tigres</strong>: porque lo que haga Tigres determina si Tiburones puede soñar con el 28.</li>
  <li><strong>No ignores Tiburones–Leones</strong>: aunque Caracas esté fuera, ese juego es vida o muerte para La Guaira.</li>
</ol>

