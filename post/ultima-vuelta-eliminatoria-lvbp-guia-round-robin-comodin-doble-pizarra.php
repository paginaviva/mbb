<h2>La pelea por los cupos directos: Águilas y el efecto dominó del 3°/4°</h2>

<p>Al amanecer del 27/12, <strong>Águilas del Zulia</strong> aparece 28-27, metida en el embudo que define el Top 4. El resumen práctico es así:</p>

<ul>
  <li><strong>Si Águilas gana en Puerto La Cruz</strong>, <strong>clasifica al Round Robin</strong>.</li>
  <li>Si gana <strong>y</strong> <strong>Navegantes del Magallanes</strong> pierde, <strong>Zulia termina 3°</strong>.</li>
  <li>Si gana <strong>y</strong> <strong>Magallanes gana</strong>, <strong>Zulia termina 4°</strong> por criterio de desempate (serie particular) frente a Magallanes.</li>
  <li><strong>Si Águilas pierde</strong>, necesita <strong>derrota de Magallanes</strong> para clasificar, apoyándose en el criterio de desempate de <strong>“llegar primero” a cierto número de victorias</strong> (quién alcanzó el umbral en menos juegos), antes de mirar series particulares.</li>
</ul>

<p>Este punto es el que más confunde al fanático en la grada: no siempre un empate se decide por “quién le ganó a quién”; muchas veces el primer filtro es <strong>cuándo llegaste a la victoria X</strong> en el calendario.</p>

<h2>El Comodín: 5° y 6° no son premios, son un pasillo estrecho</h2>

<p>Dos realidades ya están bien marcadas:</p>

<ul>
  <li><strong>Magallanes</strong> aseguró al menos el <strong>5° lugar</strong>: eso es <strong>Comodín en casa</strong> (y todavía sueña con evitarlo si se le acomodan las combinaciones del día).</li>
  <li><strong>Tigres de Aragua</strong> amaneció en el <strong>6° lugar</strong>, pero no está “a salvo” emocionalmente: está en la zona donde cada inning define si sigues o si te toca escuchar enero por televisión.</li>
</ul>

<p>Y ahí aparece el equipo que llega con la ruta más cruel: <strong>Tiburones de La Guaira</strong>.</p>

<h2>Tiburones: ganar y esperar, con la sombra real de un juego extra</h2>

<p>La Guaira amaneció <strong>26-29</strong>, a un juego de Tigres (27-28). Su escenario está escrito sin adornos:</p>

<ul>
  <li><strong>Si Tiburones pierde</strong>, queda <strong>eliminado</strong>.</li>
  <li>Si <strong>Tiburones gana</strong> pero <strong>Tigres también gana</strong>, <strong>no le alcanza</strong>: se queda fuera igual.</li>
  <li>Para mantener vida: <strong>Tiburones debe ganar y Tigres debe perder</strong>. Esa combinación forzaría un <strong>juego extra</strong> el <strong>domingo 28</strong> en <strong>Maracay</strong>, con sede definida por el criterio de <strong>“victorias alcanzadas en menos juegos”</strong>.</li>
</ul>

<p>Ese detalle es importante: la liga contempla juegos extra cuando el empate afecta el acceso al Comodín, precisamente para que el terreno resuelva lo que sería demasiado pesado decidir solo por escritorio.</p>

<h2>Lo que hay que mirar hoy: una guía corta para no perderse</h2>

