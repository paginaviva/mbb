<h2>Qué se sabe del “mapa” de participantes</h2>

<p>En lo esencial, el relato coincide: la Serie de las Américas apunta a reunir <strong>múltiples países</strong> y a darle forma a un cuadro internacional que no depende del Caribe tradicional. Entre los nombres que se han mencionado como parte del ecosistema competitivo aparecen ligas o representaciones asociadas a <strong>Colombia, Curazao, Nicaragua, Panamá, Cuba, Argentina, Brasil y Venezuela</strong>, con la idea de completar un torneo de <strong>ocho banderas</strong>.</p>

